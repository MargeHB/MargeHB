package desafio8;

public class Calculadora {
	

	//método SUMAR
	public int sumar (int a, int b) {
			return a+b;
	}
	
	//Método RESTAR
	public int restar(int c, int d) {
		return c-d;
	}
	
	public int multiplicar(int e, int f) {
		return e*f;
	}
	
	public float dividir (int g, int h) {
		return (g/h);
	}

}
